package org.java.variables;

public class DataTypes 
{

	public static void main(String[] args) 
	{
		
		/*
		 * int:-->  Numeric datatype accept only numbers to store
		 * 			Default value is  0
		 */
		int a=732947239;
		int b=463463444;
		int c=a+b;
		System.out.println("total value is "+c);
		
		
		/*
		 * boolean:===>  Accept only true/false
		 * 				 default value is false
		 */
		boolean flag=true;
		System.out.println("Flag status is  => "+flag);
		
		
		/*
		 * String :--> Allow set of characters to store
		 * 			   default value is null.
		 * 			   
		 * 		Note:--> String is a nonprimitive data type and
		 * 				also called as  class.
		 */
		String name="mqdsnr";
		String mobile="9030248855";
		String Account="121245457878";
		String email="info.dsnr@gmail.com";
		String Address="#310, Sreeman rama towers";
		
		System.out.println(name+"  "+mobile+"  "+Account+"   "+email+"   "+Address);
		
		
	}

}
